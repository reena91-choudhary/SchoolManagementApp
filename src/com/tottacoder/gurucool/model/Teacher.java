package com.tottacoder.gurucool.model;


public class Teacher {
	
	
	private int teacherId;
	
	private String name;
	
	public int getId() {
		return teacherId;
	}
	
	public void setId(int teacherId) {
		this.teacherId = teacherId;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}

}
